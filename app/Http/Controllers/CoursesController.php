    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
         $this->validate($request, [
            'course' => 'required',
            
            'file' => 'mimes:doc,pdf,PDF,docx,zip,ppt,pps,rar,ace,tar,gz,cab,cap,xls'
        ]);

        // Handle File Upload
        if($request->hasFile('file')){
            // Get filename with the extension
            $filenameWithExt = $request->file('file')->getClientOriginalName();
            // Get just filename
            $filename = pathinfo($filenameWithExt, PATHINFO_FILENAME);
            // Get just ext
            $extension = $request->file('file')->getClientOriginalExtension();
            // Filename to store
            $fileNameToStore= $filename.'_'.time().'.'.$extension;
            // Upload Image
            $path = $request->file('file')->storeAs('/public/file', $fileNameToStore);
        } else {
            $fileNameToStore = 'none';
        }

        // Create Post
        $courses = new Courses;
        $courses->course = $request->input('course');

        $courses->user_id = auth()->user()->id;
        $courses->file = $fileNameToStore;
        $courses->save();

        return redirect('/courses')->with('success', 'courses Created');
    }

